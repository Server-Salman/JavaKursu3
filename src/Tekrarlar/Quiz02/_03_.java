public class _03_ {
    public static void main(String[] args) {

        int arr[][] = new int[2][3];
        for (int i = 0; i < 1; ++i) {
            for (int j = 0; j < 3; ++j) {
                arr[i][j] = j;
                System.out.print(arr[i][j]);
            }
        }
    }
}
