package Gun09;

public class _05_JavaAssignmentOperators {
    public static void main(String[] args) {
        int a=10;

        a=a+5;   // a nın değerini 5 artır.
        a+=5;    // bu da aynı a nın artış değer 5 eşit,aynı

        a=a-5;  // a nın değerini 5 azaltır.
        a-=5;   // bu da aynı kısa yazılışı

        a=a*5;  // a nın değerini 5 le çarpar.
        a*=5;   // bu da aynı

        a=a/5;  // a yı 5  böl
        a/=5;   // bu da aynı
    }
}
