package Gun02;

public class _02_Ornek1 {
    public static void main(String[] args) {
        System.out.println("Merhaba Dünya");
        // print in sonundaki ln yani yazdıktan sonra alt satıra geç
        System.out.print("Java ya Hoşgeldiniz");
        System.out.println("Admız Java");

        System.out.println("Merhaba\nDünya"); //   \n yeni satır açar aynı ln gibi
        /* Pazartesi kelimesinin her harfini ayrı satıra yazınız  */

        System.out.println("P\na\nz\na\nr\nt\ne\ns\ni");
        System.out.println();
        System.out.println("S");
        System.out.println("a");
        System.out.println("l");
        System.out.println("ı");
    }
}
